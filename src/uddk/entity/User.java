package uddk.entity;

/**
 *
 * @author USER
 */
public class User {

    private String id;
    private String name;
    private String position;
    private int age;
    private String username;
    private String password;
    private int role;

    public User() {
    }

    public User(String id, String name, String position, int age, String username, String password, int role) {
        this.id = id;
        this.name = name;
        this.position = position;
        this.age = age;
        this.username = username;
        this.password = password;
        this.role = role;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

}
